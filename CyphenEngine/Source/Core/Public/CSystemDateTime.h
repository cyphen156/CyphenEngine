#pragma once

// ============================================================================
// CSystemDateTime
// ----------------------------------------------------------------------------
// OS에서 가져온 날짜/시간 값을 엔진 공통 형식으로 담기 위한 구조체입니다.
//
// Windows의 SYSTEMTIME, Linux / macOS 계열의 tm / timespec 같은 플랫폼별
// 시간 구조체를 Core 계층으로 직접 노출하지 않기 위해 사용합니다.
//
// 이 구조체는 시간 정책이나 문자열 포맷 책임을 가지지 않습니다.
// Local / UTC 시스템 날짜와 시간을 전달하기 위한 순수 값 타입입니다.
// ============================================================================

struct CSystemDateTime
{
	int year = 0;
	int month = 0;
	int day = 0;

	int hour = 0;
	int minute = 0;
	int second = 0;
	int millisecond = 0;
};
