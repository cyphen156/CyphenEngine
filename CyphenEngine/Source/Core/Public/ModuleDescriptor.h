#pragma once

#include "Core/Public/CString.h"

// ============================================================================
// ModuleDescriptor
// ----------------------------------------------------------------------------
// 설치된 엔진 모듈을 기술하는 선언형 값입니다.
//
// moduleName:
//   플랫폼 확장자를 제외한 논리적 모듈 이름입니다.
//
// isEnabled:
//   사용자 설정에 따라 이번 실행에서 모듈을 활성화할지 나타냅니다.
//   false인 모듈도 설치된 모듈 목록에는 유지됩니다.
// ============================================================================

struct ModuleDescriptor
{
	CString moduleName;
	bool isEnabled = false;
};
